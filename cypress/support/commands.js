import Shop from "../PageObject/Shop"

Cypress.Commands.add("addItem", (itemTitle)=>{
    const shop = new Shop()
    shop.getTitle().each((e1, index)=>{
        if(e1.text().includes(itemTitle)){
            shop.addButton().eq(index).click()
        }
    })
})




